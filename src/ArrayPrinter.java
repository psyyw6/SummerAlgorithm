public class ArrayPrinter {
    public static void print(int[] array){
        for(int i : array){
            System.out.print(i+" ");
        }
        System.out.println();
    }

    public static void print(double[] array){
        for(double i : array){
            System.out.print(i+" ");
        }
        System.out.println();
    }
}
